package ch02;

public class Test03 {
  public static void main(String[] args) {
    // 1,234,567,890 정수 값을 자릿수를 포함하여 표현해보라
    // => 3자리마다 자릿수 표기
    System.out.println(1_234_567_890);
    
    // => 4자리마다 자릿수 표기
    System.out.println(12_3456_7890);
    
    // => 2진수 자릿수를 포함하여 표현
    // => 4비트마다 자릿수 표기할 것
    System.out.println(0b0110_0001);
  }

}
